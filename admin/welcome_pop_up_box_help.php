    http://example.com/hotlink-ok/images/pic.jpg
    http://example.com/images/main-site/hotlink-ok/pic.jpg
Hotlink Protection prevents your images from being used by other sites. This can reduce the bandwidth consumed by your origin server. When enabled, Cloudflare Hotlink Protection Open 
external link blocks all HTTP referrers that are not part of your domain or zone. You can allow certain images to be hotlinked by placing them in a directory named 
hotlink-ok as seen above. The hotlink-ok directory can be placed anywhere on your 
website. Welcome Popup Box show welcome pop-up box on page. It is great-looking and responsive!. 
